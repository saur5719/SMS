    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Assign Total Fee
        String id=JOptionPane.showInputDialog(this,"Enter Student ID");
        String total=JOptionPane.showInputDialog(this,"Enter Total Fee");
        int paid=0;
        String remain=total;
        try{
            String sql="insert into fee (sid,totalfee,paidfee,remainingfee)values('"+id+"','"+total+"',"+paid+",'"+remain+"')";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(this,"Your record is saved successfully!! ");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

