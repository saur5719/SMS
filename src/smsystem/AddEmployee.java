    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Add employee Information
        try{
            String firstname=jTextField1.getText();
            String lasttname=jTextField2.getText();
            String position=jTextField3.getText();
            String age=jTextField4.getText();
            String contact=jTextField6.getText();
            String emailid=jTextField7.getText();
            String gender="";
            if(jRadioButton1.isSelected()){
                gender="Male";
            }
            else if(jRadioButton2.isSelected()){
                gender="Female";
            }
            String permanentaddress=jTextArea1.getText();
            String presentaddress=jTextArea2.getText();
            //query for save data into database
            String sql="insert into employee (firstname,lastname,position,age,contact,emailid,gender,permanentaddress,presentaddress) values('"+firstname+"','"+lasttname+"','"+position+"','"+age+"','"+contact+"','"+emailid+"','"+gender+"','"+permanentaddress+"','"+presentaddress+"')";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(this,"Your record is saved successfully!! ");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

