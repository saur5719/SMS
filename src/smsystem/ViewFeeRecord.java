private void connect() {
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mmmut","root","");
            stmt = con.createStatement();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
//display data on load of page
private void loadData() {
        try{
            String sql="select * from fee ";
            rs = stmt.executeQuery(sql);
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            while(rs.next()){
                model.addRow(new Object[]{rs.getString("id"),rs.getString("sid"),rs.getString("totalfee"),rs.getString("paidfee"),rs.getString("remainingfee")});
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
